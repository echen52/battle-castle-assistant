// Function to get sprite URL
function getPokemonSpriteUrl(species) {
    const id = POKEMON_ID_MAP[species];
    if (!id) {
        return null; // Return null if Pokemon not found
    }
    return `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/${id}.png`;
}

